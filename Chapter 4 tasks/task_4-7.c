#include <stdio.h>

/**
 * main - sum of numbers in multi table 8
 * Return: Always 0;
 */

int main(void)
{
    int i, j, prod, sum = 0;

    if(i = 8)
    {
        for(j = 1; j <= 10; j++)
        {
            prod = i * j;
            sum = sum + prod;
            printf("Product of 8 is %d\n", j, prod);
        }
        printf("sum of the products of 8 is %d", sum);
    }
    return(0);
}